// FUNCTIONS THAT RETURNS AN OBJECT WITH THE CONTENT OF THE MESSAGE AND THE TIME IT WAS CREATED AT
const generateMessage = (username, text) => {
    return {
        username,
        text,
        createdAt: new Date().getTime()
    }
}

const generateLocationMessage = (username, url) => {
    return {
        username,
        url,
        createdAt: new Date().getTime()
    }
}

module.exports = {
    generateMessage,
    generateLocationMessage
}